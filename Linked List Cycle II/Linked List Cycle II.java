/**
 * Definition for singly-linked list.
 * class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) {
 *         val = x;
 *         next = null;
 *     }
 * }
 */
public class Solution {
    public ListNode detectCycle(ListNode head) {
        List<ListNode>l=new LinkedList<>();
        while (head != null && head.next != null) {
            if(l.contains(head)) return head;
            l.add(head);
            head = head.next;
        }
        return null;
    }
}
